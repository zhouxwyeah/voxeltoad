// offsetEnvelope wraps a page payload for offset-paginated endpoints, mirroring
// the enterprise admin API's {data, total, page, page_size} contract so the
// reused UI components need no remapping.
func offsetEnvelope(data any, total int64, page, pageSize int) map[string]any {
	return map[string]any{
		"data":      data,
		"total":     total,
		"page":      page,
		"page_size": pageSize,
	}
}

func writeJSON(w http.ResponseWriter, status int, v any) {
	w.Header().Set("Content-Type", "application/json")
	w.WriteHeader(status)
	_ = json.NewEncoder(w).Encode(v)
}

func writeError(w http.ResponseWriter, status int, msg string) {
	writeJSON(w, status, map[string]string{"error": msg})
}

const (
	defaultPageSize = 20
	maxPageSize     = 100
	maxPage         = 500
	defaultLimit    = 50
	maxLimit        = 500
)

func parsePage(q map[string][]string) int {
	return clampInt(firstQuery(q, "page"), 1, maxPage, 1)
}

func parsePageSize(q map[string][]string) int {
	return clampInt(firstQuery(q, "page_size"), 1, maxPageSize, defaultPageSize)
}

func parseLimit(q map[string][]string) int {
	return clampInt(firstQuery(q, "limit"), 1, maxLimit, defaultLimit)
}

func firstQuery(q map[string][]string, key string) string {
	if v := q[key]; len(v) > 0 {
		return v[0]
	}
	return ""
}

func clampInt(s string, lo, hi, def int) int {
	n, err := strconv.Atoi(s)
	if err != nil || n < lo {
		return def
	}
	if n > hi {
		return hi
	}
	return n
}

// parseTimeRange reads optional RFC3339 from/to query params. Absent params are
// zero times (unbounded). Malformed values → 400 and ok=false.
func parseTimeRange(w http.ResponseWriter, r *http.Request) (from, to time.Time, ok bool) {
	q := r.URL.Query()
	if s := q.Get("from"); s != "" {
		t, err := time.Parse(time.RFC3339, s)
		if err != nil {
			writeError(w, http.StatusBadRequest, "invalid from timestamp (want RFC3339)")
			return time.Time{}, time.Time{}, false
		}
		from = t
	}
	if s := q.Get("to"); s != "" {
		t, err := time.Parse(time.RFC3339, s)
		if err != nil {
			writeError(w, http.StatusBadRequest, "invalid to timestamp (want RFC3339)")
			return time.Time{}, time.Time{}, false
		}
		to = t
	}
	return from, to, true
}

