func (s *Server) handleHealth(w http.ResponseWriter, r *http.Request) {
	writeJSON(w, http.StatusOK, map[string]string{"status": "ok"})
}

func (s *Server) handleRequestLogs(w http.ResponseWriter, r *http.Request) {
	q := r.URL.Query()
	from, to, ok := parseTimeRange(w, r)
	if !ok {
		return
	}
	rows, total, err := s.repo.ListRequestLogs(r.Context(), desktopstore.RequestLogFilter{
		Provider:       q.Get("provider"),
		ModelRequested: q.Get("model_requested"),
		ErrorType:      q.Get("error_type"),
		SessionID:      q.Get("session_id"),
		RequestID:      q.Get("request_id"),
		AgentType:      q.Get("agent_type"),
		From:           from,
		To:             to,
	}, parsePage(q), parsePageSize(q))
	if err != nil {
		writeError(w, http.StatusInternalServerError, "query failed")
		return
	}
	writeJSON(w, http.StatusOK, offsetEnvelope(rows, total, parsePage(q), parsePageSize(q)))
}

func (s *Server) handleSessions(w http.ResponseWriter, r *http.Request) {
	q := r.URL.Query()
	from, to, ok := parseTimeRange(w, r)
	if !ok {
		return
	}
	sessions, total, err := s.repo.ListSessions(r.Context(), desktopstore.SessionListFilter{
		AgentType: q.Get("agent_type"),
		From:      from,
		To:        to,
	}, parsePage(q), parsePageSize(q))
	if err != nil {
		writeError(w, http.StatusInternalServerError, "query failed")
		return
	}
	writeJSON(w, http.StatusOK, offsetEnvelope(sessions, total, parsePage(q), parsePageSize(q)))
}

func (s *Server) handleOverview(w http.ResponseWriter, r *http.Request) {
	from, to, ok := parseTimeRange(w, r)
	if !ok {
		return
	}
	agents, totals, err := s.repo.Overview(r.Context(), from, to)
	if err != nil {
		writeError(w, http.StatusInternalServerError, "query failed")
		return
	}
	writeJSON(w, http.StatusOK, map[string]any{
		"agents": agents,
		"totals": totals,
	})
}

func (s *Server) handleTraceBySession(w http.ResponseWriter, r *http.Request) {
	sessionID := r.PathValue("session_id")
	if sessionID == "" {
		writeError(w, http.StatusBadRequest, "session_id path parameter is required")
		return
	}
	rows, err := s.repo.ListTraceBySession(r.Context(), sessionID, parseLimit(r.URL.Query()))
	if err != nil {
		writeError(w, http.StatusInternalServerError, "query failed")
		return
	}
	writeJSON(w, http.StatusOK, map[string]any{
		"session_id": sessionID,
		"requests":   rows,
	})
}

func (s *Server) handleTraceByRowID(w http.ResponseWriter, r *http.Request) {
	id, err := strconv.ParseInt(r.PathValue("id"), 10, 64)
	if err != nil || id <= 0 {
		writeError(w, http.StatusBadRequest, "id must be a positive integer")
		return
	}
	d, ok, err := s.repo.GetTraceByRowID(r.Context(), id)
	if err != nil {
		writeError(w, http.StatusInternalServerError, "query failed")
		return
	}
	if !ok {
		writeError(w, http.StatusNotFound, "trace not found")
		return
	}
	writeJSON(w, http.StatusOK, d)
}

func (s *Server) handleTraceByRequestID(w http.ResponseWriter, r *http.Request) {
	requestID := r.PathValue("request_id")
	if requestID == "" {
		writeError(w, http.StatusBadRequest, "request_id path parameter is required")
		return
	}
	d, ok, err := s.repo.GetTraceByRequestID(r.Context(), requestID)
	if err != nil {
		writeError(w, http.StatusInternalServerError, "query failed")
		return
	}
	if !ok {
		writeError(w, http.StatusNotFound, "trace not found")
		return
	}
	writeJSON(w, http.StatusOK, d)
}

