// configDisabled guards every config handler. Returns true (and has written the
// 503) when the server was built without a configPath/watcher.
func (s *Server) configDisabled(w http.ResponseWriter) bool {
	if s.configPath == "" || s.watcher == nil {
		writeError(w, http.StatusServiceUnavailable, "config CRUD disabled (server in read-only mode)")
		return true
	}
	return false
}

// loadConfig reads the current YAML into a Dynamic pointer. On read/parse
// failure it returns 500 (the file should have been valid at startup; a mid-run
// corruption is an operator-visible error, not a not-found).
func (s *Server) loadConfig(w http.ResponseWriter) *config.Dynamic {
	dyn, err := desktopcfg.LoadFromFile(s.configPath)
	if err != nil {
		writeError(w, http.StatusInternalServerError, "read config: "+err.Error())
		return nil
	}
	return dyn
}

// saveConfigAndReload is the post-write path shared by every POST/PUT/DELETE:
// bump version (so DispatcherWatcher.rebuild sees a change) -> atomic write ->
// rebuild dispatcher (force). Rebuild failure is logged but NOT returned as an
// error: the file is already updated, and keeping the last-good dispatcher is
// the documented behavior (watcher.go:71-73). The API returns 200 with a
// warning field so the UI can surface "saved but live-reload failed".
func (s *Server) saveConfigAndReload(w http.ResponseWriter, dyn *config.Dynamic) (warning string, ok bool) {
	dyn.Version = "desktop-" + time.Now().UTC().Format("20060102-150405.000")
	if err := desktopcfg.Save(s.configPath, dyn); err != nil {
		writeError(w, http.StatusInternalServerError, "write config: "+err.Error())
		return "", false
	}
	if err := s.watcher.Build(); err != nil {
		// Config landed on disk; the live dispatcher keeps the previous good
		// state. Surface a warning so the user knows to fix + reload.
		return "saved but hot-reload failed: " + err.Error(), true
	}
	return "", true
}

// --- providers ---

func (s *Server) handleListProviders(w http.ResponseWriter, r *http.Request) {
	if s.configDisabled(w) {
		return
	}
	dyn := s.loadConfig(w)
	if dyn == nil {
		return
	}
	writeJSON(w, http.StatusOK, dyn.Providers)
}

func (s *Server) handleGetProvider(w http.ResponseWriter, r *http.Request) {
	if s.configDisabled(w) {
		return
	}
	name := r.PathValue("name")
	dyn := s.loadConfig(w)
	if dyn == nil {
		return
	}
	for _, p := range dyn.Providers {
		if p.Name == name {
			writeJSON(w, http.StatusOK, p)
			return
		}
	}
	writeError(w, http.StatusNotFound, "provider not found")
}

func (s *Server) handleCreateProvider(w http.ResponseWriter, r *http.Request) {
	if s.configDisabled(w) {
		return
	}
	var p config.Provider
	if !readJSON(w, r, &p) {
		return
	}
	if p.Name == "" {
		writeError(w, http.StatusBadRequest, "name is required")
		return
	}
	dyn := s.loadConfig(w)
	if dyn == nil {
		return
	}
	for _, existing := range dyn.Providers {
		if existing.Name == p.Name {
			writeError(w, http.StatusConflict, "provider "+p.Name+" already exists")
			return
		}
	}
	dyn.Providers = append(dyn.Providers, p)
	if warn, ok := s.saveConfigAndReload(w, dyn); ok {
		writeJSON(w, http.StatusCreated, envelope{Data: p, Warning: warn})
	}
}

func (s *Server) handleUpdateProvider(w http.ResponseWriter, r *http.Request) {
	if s.configDisabled(w) {
		return
	}
	name := r.PathValue("name")
	var p config.Provider
	if !readJSON(w, r, &p) {
		return
	}
	dyn := s.loadConfig(w)
	if dyn == nil {
		return
	}
	idx := -1
	for i, existing := range dyn.Providers {
		if existing.Name == name {
			idx = i
			break
		}
	}
	if idx < 0 {
		writeError(w, http.StatusNotFound, "provider not found")
		return
	}
	// Name in URL wins (renames via PUT are rejected to avoid breaking model/route refs).
	p.Name = name
	dyn.Providers[idx] = p
	if warn, ok := s.saveConfigAndReload(w, dyn); ok {
		writeJSON(w, http.StatusOK, envelope{Data: p, Warning: warn})
	}
}

func (s *Server) handleDeleteProvider(w http.ResponseWriter, r *http.Request) {
	if s.configDisabled(w) {
		return
	}
	name := r.PathValue("name")
	dyn := s.loadConfig(w)
	if dyn == nil {
		return
	}
	// Reference check: reject if any model upstream or route references it.
	for _, m := range dyn.Models {
		for _, u := range m.Upstreams {
			if u.Provider == name {
				writeError(w, http.StatusConflict, "provider "+name+" is referenced by model "+m.Alias)
				return
			}
		}
	}
	for _, rt := range dyn.Routes {
		for _, rp := range rt.Providers {
			if rp.Name == name {
				writeError(w, http.StatusConflict, "provider "+name+" is referenced by route "+rt.ModelAlias)
				return
			}
		}
	}
	idx := -1
	for i, existing := range dyn.Providers {
		if existing.Name == name {
			idx = i
			break
		}
	}
	if idx < 0 {
		writeError(w, http.StatusNotFound, "provider not found")
		return
	}
	dyn.Providers = append(dyn.Providers[:idx], dyn.Providers[idx+1:]...)
	if warn, ok := s.saveConfigAndReload(w, dyn); ok {
		writeJSON(w, http.StatusOK, envelope{Warning: warn})
	}
}

