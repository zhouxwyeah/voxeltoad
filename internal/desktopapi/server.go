// Package server is the desktop gateway's thin read + config API
// (design/desktop.md §10.2). It exposes:
//   - recording data captured by the data plane (request logs, session
//     aggregation, trace payloads, per-agent overview) for the UI; and
//   - CRUD over providers/models/routes backed by the local YAML file
//     (design/desktop.md §7), with hot-reload via DispatcherWatcher.Build.
//
// It reuses the query semantics of internal/admin's /request-logs and /trace/*
// endpoints and mirrors the CRUD shape of /providers /models /routes, but is
// written against the SQLite QueryRepo + a YAML file (not PG) and uses only
// the standard library (no gin) to keep the desktop binary lean. The data
// plane (proxy.Router) is mounted separately; main.go routes /api/v1/* to
// this handler and everything else to the proxy.
package desktopapi

import (
	"encoding/json"
	"net/http"
	"os"
	"path/filepath"
	"strconv"
	"time"

	desktopcfg "voxeltoad/cmd/desktop/config"
	"voxeltoad/internal/app"
	"voxeltoad/internal/config"
	"voxeltoad/internal/desktopstore"
)

// Server wires the read endpoints + config CRUD. The configPath + watcher are
// nil-safe: when zero, config endpoints return 503 (desktop started without a
// writable config, e.g. in some tests). Read endpoints always work.
type Server struct {
	repo       *desktopstore.QueryRepo
	configPath string
	watcher    *app.DispatcherWatcher
}

// New builds the read API server over the given SQLite connection. configPath
// and watcher enable the /api/v1/{providers,models,routes} CRUD endpoints with
// hot-reload; pass empty/nil to disable them (read-only mode).
func New(db *desktopstore.DB, configPath string, watcher *app.DispatcherWatcher) *Server {
	return &Server{
		repo:       desktopstore.NewQueryRepo(db),
		configPath: configPath,
		watcher:    watcher,
	}
}

// Handler returns the HTTP handler for the API. Mount it at /api/v1/.
func (s *Server) Handler() http.Handler {
	mux := http.NewServeMux()
	mux.HandleFunc("GET /api/v1/health", s.handleHealth)
	mux.HandleFunc("GET /api/v1/request-logs", s.handleRequestLogs)
	mux.HandleFunc("GET /api/v1/sessions", s.handleSessions)
	mux.HandleFunc("GET /api/v1/overview", s.handleOverview)
	mux.HandleFunc("GET /api/v1/trace/sessions/{session_id}", s.handleTraceBySession)
	mux.HandleFunc("GET /api/v1/trace/rows/{id}", s.handleTraceByRowID)
	// request_id values generated by the data plane contain a slash
	// (e.g. "NODE/abc-000001"), so a single-segment wildcard would 404. The
	// "..." multi-segment wildcard (Go 1.22+) captures the full remainder.
	mux.HandleFunc("GET /api/v1/trace/requests/{request_id...}", s.handleTraceByRequestID)

	// Config CRUD (providers / models / routes) + manual reload. Disabled when
	// the server was built without a configPath/watcher (read-only mode).
	if s.configPath != "" && s.watcher != nil {
		mux.HandleFunc("GET /api/v1/providers", s.handleListProviders)
		mux.HandleFunc("POST /api/v1/providers", s.handleCreateProvider)
		mux.HandleFunc("GET /api/v1/providers/{name}", s.handleGetProvider)
		mux.HandleFunc("PUT /api/v1/providers/{name}", s.handleUpdateProvider)
		mux.HandleFunc("DELETE /api/v1/providers/{name}", s.handleDeleteProvider)

		mux.HandleFunc("GET /api/v1/models", s.handleListModels)
		mux.HandleFunc("POST /api/v1/models", s.handleCreateModel)
		mux.HandleFunc("GET /api/v1/models/{alias}", s.handleGetModel)
		mux.HandleFunc("PUT /api/v1/models/{alias}", s.handleUpdateModel)
		mux.HandleFunc("DELETE /api/v1/models/{alias}", s.handleDeleteModel)

		mux.HandleFunc("GET /api/v1/routes", s.handleListRoutes)
		mux.HandleFunc("POST /api/v1/routes", s.handleCreateRoute)
		mux.HandleFunc("GET /api/v1/routes/{alias}", s.handleGetRoute)
		mux.HandleFunc("PUT /api/v1/routes/{alias}", s.handleUpdateRoute)
		mux.HandleFunc("DELETE /api/v1/routes/{alias}", s.handleDeleteRoute)

		mux.HandleFunc("POST /api/v1/config/reload", s.handleReload)
	}
	return mux
}

